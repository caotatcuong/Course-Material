function [I,rEI] = Gauss(f,a,k)
if k == 2 % 2 diem nut
    w = [1, 1];
    xx = [-0.577350269189626, 0.577350269189626];

elseif k == 3 % 3 diem nut
    w = [0.555555555555556, 0.888888888888889 ,0.555555555555556];
    xx = [-0.774596669241483, 0, 0.774596669241483];
    
else %(k=4) 4 diem nut
    w = [0.347854845137454, 0.652145154862546, 0.652145154862546, 0.347854845137454];
    xx = [-0.861136311594053, -0.339981043584856, 0.339981043584856, 0.861136311594053];
end 
t = (a(2) - a(1))/2 * xx + (a(2) + a(1))/2;
I = 0;
for i = 1:k
    I = I + w(i) * f(t(i));
end
I = (a(2) - a(1))/2 * I;
end

